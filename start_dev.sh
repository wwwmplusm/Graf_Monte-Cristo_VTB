#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –∑–∞–ø—É—Å–∫–∞ Backend –∏ Frontend –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./start_dev.sh

set -e

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${BLUE}üöÄ –ó–∞–ø—É—Å–∫ FinPulse –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è...${NC}\n"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
cd "$SCRIPT_DIR"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è
if [ ! -d "venv" ]; then
    echo -e "${YELLOW}‚ö†Ô∏è  –í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ. –°–æ–∑–¥–∞—é...${NC}"
    python3 -m venv venv
fi

# –ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è
echo -e "${BLUE}üì¶ –ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è...${NC}"
source venv/bin/activate

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π backend
if ! python -c "import fastapi" 2>/dev/null; then
    echo -e "${YELLOW}‚ö†Ô∏è  –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π backend...${NC}"
    pip install -r hktn/requirements.txt
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π frontend
if [ ! -d "hktn/node_modules" ]; then
    echo -e "${YELLOW}‚ö†Ô∏è  –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π frontend...${NC}"
    cd hktn
    npm install
    cd ..
fi

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ –ø—Ä–∏ –≤—ã—Ö–æ–¥–µ
cleanup() {
    echo -e "\n${YELLOW}üõë –û—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–µ—Ä–≤–µ—Ä–æ–≤...${NC}"
    kill $BACKEND_PID 2>/dev/null || true
    kill $FRONTEND_PID 2>/dev/null || true
    exit 0
}

trap cleanup SIGINT SIGTERM

# –ó–∞–ø—É—Å–∫ Backend
echo -e "${GREEN}üîß –ó–∞–ø—É—Å–∫ Backend –Ω–∞ http://localhost:8000${NC}"
cd hktn
uvicorn hktn.backend_app:app --reload --port 8000 > ../backend.log 2>&1 &
BACKEND_PID=$!
cd ..

# –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ backend
sleep 3

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —á—Ç–æ backend –∑–∞–ø—É—Å—Ç–∏–ª—Å—è
if ! kill -0 $BACKEND_PID 2>/dev/null; then
    echo -e "${YELLOW}‚ùå Backend –Ω–µ –∑–∞–ø—É—Å—Ç–∏–ª—Å—è. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ backend.log${NC}"
    exit 1
fi

# –ó–∞–ø—É—Å–∫ Frontend
echo -e "${GREEN}üé® –ó–∞–ø—É—Å–∫ Frontend –Ω–∞ http://localhost:5173${NC}"
cd hktn
npm run dev > ../frontend.log 2>&1 &
FRONTEND_PID=$!
cd ..

# –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—É—Å–∫–∞ frontend
sleep 3

echo -e "\n${GREEN}‚úÖ –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –∑–∞–ø—É—â–µ–Ω–æ!${NC}\n"
echo -e "${BLUE}üìç Backend:  ${NC}http://localhost:8000"
echo -e "${BLUE}üìç Frontend: ${NC}http://localhost:5173"
echo -e "${BLUE}üìç API Docs: ${NC}http://localhost:8000/docs"
echo -e "\n${YELLOW}üìù –õ–æ–≥–∏:${NC}"
echo -e "   Backend:  tail -f backend.log"
echo -e "   Frontend: tail -f frontend.log"
echo -e "\n${YELLOW}–ù–∞–∂–º–∏—Ç–µ Ctrl+C –¥–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏${NC}\n"

# –û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
wait

